package assignment.day5;

public class ForLoopDemo {

	public static void main(String[] args) {
		
		for(int i=1;i<=10;i++)
		{
			System.out.println(i);
		}
		System.out.println("\n");
		
		for(int i=5;i<=50;i=i+5)
		{
			System.out.println(i);
		}
		
		System.out.println("\n");
		
		for(int i=9;i<=99;i=i+10)
		{
			System.out.println(i);
		}
	}

}
