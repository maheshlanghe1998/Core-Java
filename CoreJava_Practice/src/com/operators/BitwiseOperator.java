package com.operators;

public class BitwiseOperator {
      //Bitwise Operators are &,|,^ ,>>,<<,>>>,~,etc.
	public static void main(String[] args) 
	{
	int a=10;
	int b=12;
	System.out.println(a&b);
	System.out.println(a|b);
	System.out.println(a>>b);
	System.out.println(a<<b);
	System.out.println(a>>>b);
	System.out.println(a^b);
	System.out.println(~a);
	}

}
