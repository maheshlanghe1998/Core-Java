package com.operators;

public class RelationalOperator {
    //Relational operator ==,<,>,<=,>=,!= are gives the boolean result. 
	public static void main(String[] args) {
	
	int a=10;
	int b=20;
	System.out.println(a==b);
	System.out.println(a<b);
	System.out.println(a>b);
	System.out.println(a<=b);
	System.out.println(a>=b);
	System.out.println(a!=b);
	}

}
